    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        
        
        int id = Integer.parseInt(jTextField1.getText().trim());
        String nom = jTextField2.getText();
        String espece = jTextField3.getText();
        int age = Integer.parseInt(jTextField4.getText());
        String regimeAlimentaire = jTextField5.getText();

        try {

            if (nom.isEmpty() || espece.isEmpty() || regimeAlimentaire.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Tous les champs sont obligatoires.", "Erreur", JOptionPane.ERROR_MESSAGE);
                return;
            }

            ZooService service = new ZooService();
            service.ajouterAnimal(id,nom, espece, age, regimeAlimentaire);

            afficherAnimaux(); // Met à jour la liste après l'ajout

            // Réinitialiser les champs
            jTextField1.setText("");
            jTextField2.setText("");
            jTextField3.setText("");
            jTextField4.setText("");
            jTextField5.setText("");

        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Âge invalide.", "Erreur", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

