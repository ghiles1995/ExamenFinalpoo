    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        
         int selectedRow = jTable1.getSelectedRow();
        
        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(this, "Sélectionnez un animal à modifier.", "Erreur", JOptionPane.ERROR_MESSAGE);
            return;
        }

        try {
            int id = Integer.parseInt(jTextField1.getText().trim());
            String nom = jTextField2.getText();
            String espece = jTextField3.getText();
            int age = Integer.parseInt(jTextField4.getText());
            String regimeAlimentaire = jTextField5.getText();

            if (nom.isEmpty() || espece.isEmpty() || regimeAlimentaire.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Tous les champs sont obligatoires.", "Erreur", JOptionPane.ERROR_MESSAGE);
                return;
            }

            ZooService service = new ZooService();
            service.mettreAJourAnimal(id, nom, espece, age, regimeAlimentaire);

            afficherAnimaux(); // Met à jour le tableau après modification

            // Réinitialiser les champs
            jTextField1.setText("");
            jTextField2.setText("");
            jTextField3.setText("");
            jTextField4.setText("");
            jTextField5.setText("");

        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Valeur invalide.", "Erreur", JOptionPane.ERROR_MESSAGE);
        }
       
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        
        int selectedRow = jTable1.getSelectedRow();

        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(this, "Sélectionnez un animal à supprimer.", "Erreur", JOptionPane.ERROR_MESSAGE);
            return;
        }

        int id = Integer.parseInt(jTable1.getValueAt(selectedRow, 0).toString());

        int confirmation = JOptionPane.showConfirmDialog(this, "Voulez-vous vraiment supprimer cet animal ?", "Confirmation", JOptionPane.YES_NO_OPTION);
        if (confirmation == JOptionPane.YES_OPTION) {
            ZooService service = new ZooService();
            service.supprimerAnimal(id);

            afficherAnimaux(); // Met à jour le tableau après suppression
        }
        
    }//GEN-LAST:event_jButton3ActionPerformed

